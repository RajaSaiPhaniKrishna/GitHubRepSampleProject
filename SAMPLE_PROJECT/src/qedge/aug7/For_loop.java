package qedge.aug7;

public class For_loop {

	public static void main(String[] args) {
	//print name for 5 times
		for (int i=1;i<=5;i++)
	{
		System.out.println("phani:::+i");
	}
	//print name for 10 times
		for (int i=1;i<=10;i++)
	{
		System.out.println("phani::::+i");
	}
	//print multiplication table-2 
		for (int i=1;i<=20;i++)
	{
		System.out.println("2*"+i+"="+2*i);
	}
			

	}
	

}
